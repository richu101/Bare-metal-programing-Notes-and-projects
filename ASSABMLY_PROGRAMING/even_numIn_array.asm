ORG 00H
MOV DPTR,#2000H
MOVX A,@DPTR
MOV R0,#05H
MOV R1,#00H
MOV R2,#00H

HERE:RRC A
	
	INC DPTR
	MOVX A,@DPTR
	JC EVCOUNT
	
	
EVCOUNT:INC R2	

DJNZ R0,HERE
MOV DPTR,#2000H
ODD:RRC A
	
	INC DPTR
	MOVX A,@DPTR
	JNC ODDCOUNT
	
	

ODDCOUNT:INC R1
DJNZ R0,ODD


MOV A,R1
INC DPTR
MOVX @DPTR,A

MOV A,R2
INC DPTR
INC DPTR
MOVX @DPTR,A






END