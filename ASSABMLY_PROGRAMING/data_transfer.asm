ORG 00H
MOV DPTR, #2000H
	MOVX A, @DPTR
	MOV R1, A
	MOV R2, A
	DEC R1
	INC DPTR
LOOP:	MOVX A, @DPTR
	MOV R4, A
	INC DPTR
	MOVX A, @DPTR
	MOV R3, A
	SUBB A, R4
	JNC SKIP
MOV A, R3
	XCH A, R4
	MOVX @DPTR, A
	DEC DPL
	MOV A, R4
	MOVX @DPTR, A
	INC DPTR
SKIP:	DJNZ R1, LOOP
	MOV DPTR,#2001H
	DEC R2
	MOV A, R2
	MOV R1, A
	DJNZ R2, LOOP
	END
